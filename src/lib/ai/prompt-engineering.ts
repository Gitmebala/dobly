/**
 * Anti-AI Prompt Engineering
 * Prompts and techniques to generate human-like text that avoids AI detection patterns
 */

export interface AntiAIPromptConfig {
  personality: "casual" | "professional" | "academic" | "creative";
  tone: "direct" | "friendly" | "formal" | "conversational";
  complexity: "simple" | "moderate" | "complex";
  constraints: string[];
}

/**
 * Generate system prompt that avoids AI patterns
 */
export function generateAntiAISystemPrompt(config: AntiAIPromptConfig): string {
  const baseConstraints = [
    "Write like a human, not an AI",
    "Use simple, direct language",
    "Avoid corporate jargon and buzzwords",
    "Be specific and concrete, not abstract",
    "Use varied sentence structures",
    "Write in active voice",
    "Keep sentences under 20 words when possible",
    "Avoid lists of three parallel items",
    "Don't use phrases like 'in conclusion', 'furthermore', 'moreover'",
    "Use 'is' and 'are' naturally",
    "Avoid words like: crucial, pivotal, enhance, showcase, delve, tapestry, landscape",
    "Be authentic and genuine",
    "Write as if talking to a friend or colleague",
  ];

  const personalityPrompts = {
    casual: "Write as if you're texting a friend. Use contractions, simple words, and a relaxed tone.",
    professional: "Write as a competent professional. Be clear, direct, and authoritative but not stuffy.",
    academic: "Write as a knowledgeable expert. Be precise but avoid academic jargon and overly complex sentences.",
    creative: "Write with imagination and flair. Use metaphors and vivid descriptions naturally.",
  };

  const tonePrompts = {
    direct: "Get straight to the point. No fluff or filler.",
    friendly: "Be warm and approachable. Use 'you' and 'we' appropriately.",
    formal: "Maintain professional distance but be clear and helpful.",
    conversational: "Write as if having a conversation. Ask questions, use natural transitions.",
  };

  const complexityPrompts = {
    simple: "Use basic vocabulary. Explain complex ideas simply.",
    moderate: "Use standard vocabulary. Mix simple and complex sentences.",
    complex: "Use precise terminology when needed, but explain it clearly.",
  };

  return `
You are a helpful assistant that writes naturally and avoids AI detection patterns.

${personalityPrompts[config.personality]}

${tonePrompts[config.tone]}

${complexityPrompts[config.complexity]}

CRITICAL CONSTRAINTS:
${baseConstraints.concat(config.constraints).map(c => `- ${c}`).join('\n')}

Remember: Your goal is to sound completely human and natural. Avoid anything that sounds like it was generated by an AI.
  `.trim();
}

/**
 * Generate user prompt template
 */
export function generateAntiAIUserPrompt(
  task: string,
  context?: string,
  examples?: string[]
): string {
  let prompt = `Task: ${task}`;

  if (context) {
    prompt += `\n\nContext: ${context}`;
  }

  if (examples && examples.length > 0) {
    prompt += `\n\nExamples of natural human writing:\n${examples.map((ex, i) => `${i + 1}. ${ex}`).join('\n')}`;
  }

  prompt += `

Please write this naturally, as a human would. Avoid:
- Corporate buzzwords and jargon
- Overly complex sentences
- Repetitive sentence structures
- AI vocabulary words
- Formal constructions that sound unnatural

Write directly and authentically.`;

  return prompt;
}

