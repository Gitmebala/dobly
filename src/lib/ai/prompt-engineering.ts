/**
 * Generate prompt with examples
 */
export function generatePromptWithExamples(
  task: string,
  category: keyof typeof NATURAL_WRITING_EXAMPLES
): string {
    const examples = NATURAL_WRITING_EXAMPLES[category];
    
    return generateAntiAIUserPrompt(
      task,
      `Here are examples of natural human ${category}:`,
      examples
    );
}
