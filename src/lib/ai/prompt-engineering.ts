/**
 * Chain-of-thought prompt to avoid AI patterns
 */
export function generateChainOfThoughtPrompt(
  topic: string,
  keyPoints: string[]
): string {
  return `
Think through this step by step, like a human would naturally process information:

1. First, consider the main topic: ${topic}
2. What are the key points to cover?
${keyPoints.map((point, i) => `   ${i + 2}. ${point}`).join('\n')}
3. How would a real person explain this to someone else?
4. What natural examples or analogies come to mind?
5. How can I structure this to flow naturally?

Now write your response thinking through these points, but write it naturally as if you're explaining it to someone. Don't follow a rigid structure - let it flow naturally.

Remember:
- Use simple, everyday language
- Be direct and clear
- Avoid AI vocabulary
- Write like you're talking to a person
`;
}

